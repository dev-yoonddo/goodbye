package member;

public class MemberDTO {

	private String memberID;
	private int groupID;
	private String userID;
	private int mbAvailable;
	private String mbContent;	
	private String mbDate;
	public String getMemberID() {
		return memberID;
	}
	public void setMemberID(String memberID) {
		this.memberID = memberID;
	}
	public int getGroupID() {
		return groupID;
	}
	public void setGroupID(int groupID) {
		this.groupID = groupID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getMbAvailable() {
		return mbAvailable;
	}
	public void setMbAvailable(int mbAvailable) {
		this.mbAvailable = mbAvailable;
	}
	public String getMbContent() {
		return mbContent;
	}
	public void setMbContent(String mbContent) {
		this.mbContent = mbContent;
	}
	public String getMbDate() {
		return mbDate;
	}
	public void setMbDate(String mbDate) {
		this.mbDate = mbDate;
	}
	
	
	
	
	

}
